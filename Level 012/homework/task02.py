# ორი ბულეანური ცვლადის განსაზღვრა
bool1 = True
bool2 = False

# ორ ბულეანს შორის AND ოპერატორი
print(f"bool1 and bool2: {bool1 and bool2}")  # AND ოპერატორი: False, რადგან bool2 False-ია

# ორ ბულეანს შორის OR ოპერატორი
print(f"bool1 or bool2: {bool1 or bool2}")   # OR ოპერატორი: True, რადგან bool1 True-ია

# სამი ბულეანური ცვლადის განსაზღვრა
bool3 = True

# სამი ბულეანის დაკავშირება AND ოპერატორით
print(f"bool1 and bool2 and bool3: {bool1 and bool2 and bool3}")  # AND ოპერატორი: False, რადგან bool2 False-ია

# სამი ბულეანის დაკავშირება OR ოპერატორით
print(f"bool1 or bool2 or bool3: {bool1 or bool2 or bool3}")  # OR ოპერატორი: True, რადგან bool1 და bool3 True-ები არია