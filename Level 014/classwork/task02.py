# მომხმარებლისთვის რიცხვების შეყვანის მოთხოვნა
პირველი_რიცხვი = int(input("3: "))
მეორე_რიცხვი = int(input("6: "))

# for ციკლი დიაპაზონზე იტერაციისთვის
for რიცხვი in range(პირველი_რიცხვი, მეორე_რიცხვი + 1):
    # საიტერაციო ცვლადის კვადრატის ბეჭდვა
    print(f"{რიცხვი}-ის კვადრატი არის {რიცხვი ** 2}")
