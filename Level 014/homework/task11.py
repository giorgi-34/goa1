# ცვლადი, რომელშიც შეინახება რიცხვები
რიცხვები = []

# for ციკლი 5-ჯერ მომხმარებლისთვის რიცხვის მოთხოვნისთვის
for i in range(5):
    # მომხმარებელისგან რიცხვის მიღება
    რიცხვი = int(input(f"შეიყვანეთ რიცხვი {i+1}-ჯერ: "))
    # რიცხვის დამატება სიაში
    რიცხვები.append(რიცხვი)

# დაბეჭდვა ყველა რიცხვისთვის
for რიცხვი in რიცხვები:
    print(რიცხვი)
