# მომხმარებლისგან რიცხვების მიღება
num1 = int(input("შეიტანეთ პირველი რიცხვი: "))
num2 = int(input("შეიტანეთ მეორე რიცხვი: "))

# პირობითი სტრუქტურა: შედარება, რომ გავარკვიოთ რომელი რიცხვია უდიდესი
if num1 > num2:
    print(f"უდიდესი რიცხვი არის: {num1}")
elif num2 > num1:
    print(f"უდიდესი რიცხვი არის: {num2}")
else:
    print("რიცხვები თანაბარია.")
