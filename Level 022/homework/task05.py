# Given list of numbers
numbers = [10, 20, 30, 40, 50, 60, 70]

# Accessing the first, third, and last elements using indexing
first_element = numbers[0]   # First element (index 0)
third_element = numbers[2]   # Third element (index 2)
last_element = numbers[-1]   # Last element (index -1)

# Printing the results
print("First element:", first_element)
print("Third element:", third_element)
print("Last element:", last_element)
