// Concatenate "Hello" and "World"
let a = "Hello".concat("World");
console.log(a); // "HelloWorld"

// Join three strings
let b = "Good".concat(" ", "Morning");
console.log(b); // "Good Morning"

// Concatenate string + number
let c = "Age: ".concat(String(25));
console.log(c); // "Age: 25"

// String + special character
let d = "Wow".concat("!");
console.log(d); // "Wow!"

// Concatenate with empty string
let e = "Test".concat("");
console.log(e); // "Test" (უცვლელი დარჩა)
