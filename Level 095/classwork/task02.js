// Replace "cat" with "dog"
let sentence1 = "The cat is sleeping";
let newSentence1 = sentence1.replace("cat", "dog");
console.log(newSentence1); // "The dog is sleeping"

// Replace first occurrence of "a" with "@"
let str3 = "banana";
let replacedA = str3.replace("a", "@");
console.log(replacedA); // "b@nana"

// Replace "world" with "JavaScript"
let sentence2 = "Hello world";
let newSentence2 = sentence2.replace("world", "JavaScript");
console.log(newSentence2); // "Hello JavaScript"
