// Extract "world" from "Hello world"
let helloWorld = "Hello world";
let worldSlice = helloWorld.slice(6, 11);
console.log(worldSlice); // "world"

// First 5 characters from "JavaScript is fun"
let jsFun = "JavaScript is fun";
let first5 = jsFun.slice(0, 5);
console.log(first5); // "JavaS"

// Remove first character from "Python"
let pythonStr = "Python";
let removedFirst = pythonStr.slice(1);
console.log(removedFirst); // "ython"
