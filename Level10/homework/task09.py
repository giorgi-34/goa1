# სამი რიცხვის შეყვანა
num1 = float(input("შეიყვანეთ პირველი რიცხვი: "))
num2 = float(input("შეიყვანეთ მეორე რიცხვი: "))
num3 = float(input("შეიყვანეთ მესამე რიცხვი: "))

# გამოთვლები
sum_result = num1 + num2 + num3             # ჯამი
difference = num1 - num2 - num3              # სხვაობა
product = num1 * num2 * num3                 # ნამრავლი
quotient = num1 / num2 / num3                # განაყოფი

# შედეგების გამოტანა
print("ჯამი:", sum_result)
print("სხვაობა:", difference)
print("ნამრავლი:", product)
print("განაყოფი:", quotient)