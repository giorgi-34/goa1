# ცვლადების შექმნა და რიცხვების შენახვა
num1 = 10
num2 = 5
num3 = 3
num4 = 2
num5 = 4

# მათემატიკური ოპერაციების მაგალითები
print(num1 + num2)                     # 10 + 5
print(num1 - num3)                     # 10 - 3
print(num2 * num4)                     # 5 * 4
print(num5 / num1)                     # 4 / 10
print(num3 * num2)                    # 3 * 5
print(num1 % num4)                     # 10 % 2
print((num1 + num2) * num3)            # (10 + 5) * 3
print(num4 * num5 - num2)              # 2 * 4 - 5
print((num1 * num2) / num4)           # (10 * 5) / 2
print(num5 + num3 * num2 - num1)       # 4 + 3 * 5 - 10