# მომხმარებლისგან რიცხვების მიღება
first_number = int(input("შეიყვანეთ პირველი რიცხვი: "))  # პირველი რიცხვის მიღება და მთელს რიცხვად გადაყვანა
second_number = int(input("შეიყვანეთ მეორე რიცხვი: "))  # მეორე რიცხვის მიღება და მთელს რიცხვად გადაყვანა

# შედეგების დაბეჭდვა
print(f"პირველი რიცხვი მეტია 30-ზე: {first_number > 30}")  # თუ პირველი რიცხვი მეტია 30-ზე
print(f"მეორე რიცხვი ნაკლებია 40-ზე: {second_number < 40}")  # თუ მეორე რიცხვი ნაკლებია 40-ზე


